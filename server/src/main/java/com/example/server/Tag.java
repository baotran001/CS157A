package com.example.server;

public class Tag {
    private String tid;
    private String tag_name;

    // Constructor
    public Tag() {
        //
    }

    // Getters and setters
    public String getTid() {
        return tid;
    }

    public void setTid(String tid) {
        this.tid = tid;
    }

    public String getTag_name() {
        return tag_name;
    }

    public void setTag_name(String tag_name) {
        this.tag_name = tag_name;
    }
}
